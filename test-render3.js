const THAI_DIGITS = ['?', '?', '?', '?', '?', '?', '?', '?', '?', '?'];

const toThaiDigits = (value) => {
  return String(value ?? "").replace(/[0-9]/g, digit => THAI_DIGITS[Number(digit)]);
}

const thaiSmartBreak = (text) => {
  if (!text) return '';
  return text
    .replace(/?\.?\.[ \t]+(\d+|[?-?]+)/g, '?.?.\u00A0$1')
    .replace(/???????[ \t]+(\d+|[?-?]+)/g, '???????\u00A0$1')
    .replace(/????????[ \t]+(\d+|[?-?]+)/g, '????????\u00A0$1')
    .replace(/????[ \t]+(\d+|[?-?]+|M|G|B)/g, '????\u00A0$1')
    .replace(/???????[ \t]+(\d+|[?-?]+)/g, '???????\u00A0$1')
    .replace(/(?\.|?\.|?\.)[ \t]+([?-?a-zA-Z]+)/g, '$1\u00A0$2')
    .replace(/[ \t]+\(([^)]+)\)/g, '\u00A0($1)')
    .replace(/(???????????????|??????????)[ \t]+(?\.?\.)/g, '$1\u00A0$2')
    .replace(/(\d+|[?-?]+)[ \t]*(?\.|??|????|???|????|????|???????|???)/g, '$1\u00A0$2')
    .replace(/????[ \t]+(\d+|[?-?]+)/g, '????\u00A0$1')
    .replace(/(??????????|?????|???|???????????????)[ \t]+([?-?a-zA-Z\d]+)/g, '$1\u00A0$2');
}

const renderText = (text) => {
  if (!text) return '';
  let formatted = text;
  formatted = formatted.replace(/^\{\{[CLR]\}\}/, '');
  formatted = formatted.replace(/\s+?\s*/g, '\n? ');
  formatted = formatted
    .replace(/\s*\|\s*/g, '\n')
    .replace(/\s+;\s+/g, '\n')
    .replace(/\s+\/\s+/g, '\n')
    .replace(/ {2,}/g, '\n');
  formatted = formatted.replace(/(???|???|??????|?????????????|??\.|??\.?\.|?\.?\.|?\.?\.|???????????|???????????????????|??????????????????????)\s+/g, '$1\u00A0');
  formatted = thaiSmartBreak(formatted);
  formatted = toThaiDigits(formatted);
  return formatted.split('\n');
}

