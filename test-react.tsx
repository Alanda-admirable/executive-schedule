import React from 'react';
import ReactDOMServer from 'react-dom/server';

const thaiSmartBreak = (text: string): string => {
  if (!text) return '';
  return text
    .replace(/[ \t]+\(([^)]+)\)/g, '\u00A0($1)')
}

const renderText = (text: string | null | undefined) => {
    if (!text) return '';
    let formatted = text;
    formatted = formatted.replace(/^\{\{[CLR]\}\}/, '');
    formatted = formatted.replace(/\s+?\s*/g, '\n? ');
    formatted = formatted
      .replace(/\s*\|\s*/g, '\n')
      .replace(/\s+;\s+/g, '\n')
      .replace(/\s+\/\s+/g, '\n')
      .replace(/ {2,}/g, '\n');
    formatted = formatted.replace(/(???|???|??????|?????????????|??\.|??\.?\.|?\.?\.|?\.?\.|???????????|???????????????????|??????????????????????)\s+/g, '$1\u00A0');
    formatted = thaiSmartBreak(formatted);
    return formatted.split('\n').map((line, i, arr) => {
      if (i === arr.length - 1) return <span key={i}>{line}</span>;
      return <span key={i}>{line}<br /></span>;
    });
}

const html = ReactDOMServer.renderToString(
  <div style={{ whiteSpace: 'pre-wrap' }}>
    {renderText("????????? ???????? ?????????????????????? ????????????????? ???????????? ?????? ?????????????????????????????????????????? ???????????? ??????? 2569 \n(Queen's Cup Thailand Championship)")}
  </div>
);

